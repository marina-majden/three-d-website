export const calculateSizes = (isSmall, isMobile, isTablet) => {
    return {
        statueScale: isSmall ? 0.75 : isMobile ? 1 : 1.35,
        statuePosition: isSmall
        ? [0, -1, 0]
        : isMobile
        ? [0, -2, 0]
        : isTablet
        ? [3, -3, 0]
        : [4, -3, 0],
    };
};

